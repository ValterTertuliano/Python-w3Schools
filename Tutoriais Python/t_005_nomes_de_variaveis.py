"""Nomes de variaveis"""

# Uma variável pode ter um nome curto (como x e y) ou um nome 
# mais descritivo (idade, nome do carro, volume_total).

# Regras para variáveis ​​Python:

# Um nome de variável deve começar com uma letra ou o 
# caractere sublinhado

# Um nome de variável não pode começar com um número

# Um nome de variável pode conter apenas caracteres 

# alfanuméricos e sublinhados (Az, 0-9 e _)

# Os nomes das variáveis ​​diferenciam maiúsculas de 
# minúsculas (idade, Idade e IDADE são três variáveis ​
# ​diferentes)

# Um nome de variável não pode ser nenhuma das palavras-chave 
# do Python .

# exemplos:
myvar = "John"
my_var = "John"
_my_var = "John"
myVar = "John"
MYVAR = "John"
myvar2 = "John"

